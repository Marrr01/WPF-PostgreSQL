        public static List<Person> GeneratePersons(int amount)
        {
            var result = new List<Person>(amount);

            for (int i = 0; i < amount; i++)
            {
                result.Add(GeneratePerson());
            }

            return result;
        }

        public static List<Person> GenerateMales(int amount)
        {
            var result = new List<Person>(amount);

            for (int i = 0; i < amount; i++)
            {
                result.Add(GenerateMale());
            }

            return result;
        }

